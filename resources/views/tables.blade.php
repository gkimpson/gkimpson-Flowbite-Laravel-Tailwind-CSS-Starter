    <script>
        // Table search functionality
        document.getElementById('table-search').addEventListener('input', function() {
            const searchTerm = this.value.toLowerCase();
            const tableRows = document.querySelectorAll('#table-search ~ .relative tbody tr');
            
            tableRows.forEach(row => {
                const text = row.textContent.toLowerCase();
                row.style.display = text.includes(searchTerm) ? '' : 'none';
            });
        });

        // Table sorting functionality
        document.querySelectorAll('.sort-btn').forEach(button => {
            button.addEventListener('click', function() {
                const column = this.getAttribute('data-column');
                const table = document.getElementById('sortable-table');
                const rows = Array.from(table.querySelectorAll('tr'));
                
                // Simple sorting logic
                rows.sort((a, b) => {
                    let aValue = '', bValue = '';
                    
                    switch(column) {
                        case 'name':
                            aValue = a.cells[0].textContent.trim();
                            bValue = b.cells[0].textContent.trim();
                            break;
                        case 'color':
                            aValue = a.cells[1].textContent.trim();
                            bValue = b.cells[1].textContent.trim();
                            break;
                        case 'category':
                            aValue = a.cells[2].textContent.trim();
                            bValue = b.cells[2].textContent.trim();
                            break;
                        case 'price':
                            aValue = parseFloat(a.cells[3].textContent.replace('$', ''));
                            bValue = parseFloat(b.cells[3].textContent.replace('$', ''));
                            break;
                    }
                    
                    if (typeof aValue === 'string') {
                        return aValue.localeCompare(bValue);
                    } else {
                        return aValue - bValue;
                    }
                });
                
                // Clear and re-populate table
                table.innerHTML = '';
                rows.forEach(row => table.appendChild(row));
            });
        });

        // Checkbox functionality
        document.getElementById('checkbox-all-search').addEventListener('change', function() {
            const checkboxes = document.querySelectorAll('tbody input[type="checkbox"]');
            checkboxes.forEach(checkbox => {
                checkbox.checked = this.checked;
            });
        });
    </script>
